// Basic Structure of loop
/**
 * 1- Starting condition
 * 2- Terminating condition
 * 3 - Incremental/Decremental Condition
 */

// Basic for loop + -> Where we need index
for (let i = 0; i <= 10; i++) {
    console.log("I: ", i);
  }
  
  // Basic reverse loop
  for (let i = 10; i >= 0; i--) {
    console.log("I-: ", i);
  }